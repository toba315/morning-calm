function parseArgs(args) {
  const parsed = {};
  for (const arg of args) {
    const [key, value] = arg.replace(/^--/, "").split("=");
    if (key === "limit") parsed.limit = Number.parseInt(value, 10);
    if (key === "detail-limit") parsed.detailLimit = Number.parseInt(value, 10);
    if (key === "timeout-ms") parsed.timeoutMs = Number.parseInt(value, 10);
  }
  return parsed;
}

